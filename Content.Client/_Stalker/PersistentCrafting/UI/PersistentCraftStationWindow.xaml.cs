    private string BuildRecipeMasteryBadge(PersistentCraftRecipePrototype recipe)
    {
        if (!TryResolveRequirementNode(recipe, out var node))
            return Loc.GetString("persistent-craft-node-mastery-short", ("level", PersistentCraftingHelper.InitialNodeMasteryLevel), ("max", PersistentCraftingHelper.MaxNodeMasteryLevel));

        return BuildNodeMasteryText(node);
    }

    private string BuildRequirementMarkup(PersistentCraftRecipePrototype recipe)
    {
        if (TryResolveRequirementNode(recipe, out var node))
        {
            var mastery = BuildNodeMasteryText(node);
            return $"[color={DescriptionText.ToHex()}]- {FormattedMessage.EscapeText(ResolveNodeName(node))} ({FormattedMessage.EscapeText(PersistentCraftingHelper.GetNodeLevelText(node))})[/color]\n" +
                   $"[color={MutedText.ToHex()}]- {FormattedMessage.EscapeText(mastery)}[/color]";
        }

        return $"[color={DescriptionText.ToHex()}]- {FormattedMessage.EscapeText(GetRequirementText(recipe))}[/color]";
    }

    private string BuildNodeMasteryText(PersistentCraftNodePrototype node)
    {
        var nodeState = GetNodeState(node);
        if (nodeState == null)
        {
            return Loc.GetString(
                "persistent-craft-node-mastery-short",
                ("level", PersistentCraftingHelper.InitialNodeMasteryLevel),
                ("max", PersistentCraftingHelper.MaxNodeMasteryLevel));
        }

        if (nodeState.ExperienceForNextLevel <= 0)
        {
            return Loc.GetString(
                "persistent-craft-node-mastery-max",
                ("level", nodeState.MasteryLevel),
                ("max", PersistentCraftingHelper.MaxNodeMasteryLevel));
        }

        return Loc.GetString(
            "persistent-craft-node-mastery-progress",
            ("level", nodeState.MasteryLevel),
            ("max", PersistentCraftingHelper.MaxNodeMasteryLevel),
            ("experience", nodeState.Experience),
            ("next", nodeState.ExperienceForNextLevel));
    }

    private bool TryResolveRequirementNode(PersistentCraftRecipePrototype recipe, out PersistentCraftNodePrototype node)
    {
        var requiredNode = string.IsNullOrWhiteSpace(recipe.RequiredNode)
            ? PersistentCraftingHelper.BuildNodeId(recipe.Branch, recipe.Tier, recipe.SubTier)
            : recipe.RequiredNode;

        PersistentCraftNodePrototype? resolvedNode;
        if (!string.IsNullOrWhiteSpace(requiredNode) &&
            _prototype.TryIndex<PersistentCraftNodePrototype>(requiredNode, out resolvedNode) &&
            resolvedNode != null)
        {
            node = resolvedNode;
            return true;
        }

        node = default!;
        return false;
    }

    private void SelectPreferredBranchTab()
    {
        if (_state == null)
            return;

        var preferred = PersistentCraftingHelper.EnumerateBranches()
            .Select(branch => new
            {
                Branch = branch,
                Recipes = _recipes.Count(recipe => recipe.Branch == branch && HasRequirement(_state, recipe)),
                Points = GetBranchState(_state, branch).AvailablePoints,
            })
            .OrderByDescending(entry => entry.Recipes)
            .ThenByDescending(entry => entry.Points)
            .FirstOrDefault(entry => entry.Recipes > 0 || entry.Points > 0);

        if (preferred != null)
            Branches.CurrentTab = GetBranchTabIndex(preferred.Branch);
    }

    private static int GetBranchTabIndex(PersistentCraftBranch branch)
    {
        return branch switch
        {
            PersistentCraftBranch.Weapon => 0,
            PersistentCraftBranch.Armor => 1,
            PersistentCraftBranch.Anomaly => 2,
            _ => 0,
        };
    }

    private string GetRecipeCategoryId(PersistentCraftRecipePrototype recipe)
    {
        if (!string.IsNullOrWhiteSpace(recipe.Category))
            return recipe.Category!;

        var displayProto = PersistentCraftingHelper.GetDisplayPrototypeId(recipe) ?? string.Empty;

        return recipe.Branch switch
        {
            PersistentCraftBranch.Weapon when displayProto.Contains("Mag") => "magazines",
            PersistentCraftBranch.Weapon when displayProto.Contains("Box") => "ammo",
            PersistentCraftBranch.Weapon => "weapons",

            PersistentCraftBranch.Armor when displayProto.StartsWith("Medkit") => "medicine",
            PersistentCraftBranch.Armor when displayProto.StartsWith("ClothingBackpack") => "bags",
            PersistentCraftBranch.Armor when displayProto.StartsWith("ClothingHeadHelmet") || displayProto.StartsWith("ClothingMask") => "headgear",
            PersistentCraftBranch.Armor => "armor",

            PersistentCraftBranch.Anomaly when displayProto.Contains("ArtifactContainer") => "containers",
            PersistentCraftBranch.Anomaly when displayProto.StartsWith("ZoneAnomalyDetector") => "anomaly-detectors",
            PersistentCraftBranch.Anomaly when displayProto.StartsWith("ZoneArtifactDetector") => "artifact-detectors",
            _ => "special",
        };
    }

    private string GetRecipeSubCategoryId(PersistentCraftRecipePrototype recipe)
    {
        if (!string.IsNullOrWhiteSpace(recipe.SubCategory))
            return recipe.SubCategory!;

        var displayProto = PersistentCraftingHelper.GetDisplayPrototypeId(recipe) ?? string.Empty;
        var category = GetRecipeCategoryId(recipe);

        if (recipe.Branch == PersistentCraftBranch.Weapon)
        {
            if (displayProto.Contains("545") || displayProto.Contains("AKS74") || displayProto.Contains("AK74") || displayProto.Contains("A545") || displayProto.Contains("AK12") || displayProto.Contains("RPK16"))
                return "caliber-545";

            if (displayProto.Contains("556") || displayProto.Contains("M16") || displayProto.Contains("M4") || displayProto.Contains("G36") || displayProto.Contains("HK416") || displayProto.Contains("SCARL") || displayProto.Contains("F2000"))
                return "caliber-556";
        }

        if (recipe.Branch == PersistentCraftBranch.Armor)
        {
            if (category == "headgear")
                return displayProto.StartsWith("ClothingMask") ? "masks" : "helmets";

            if (category == "bags")
                return "backpacks";

            if (category == "medicine")
                return "medkits";
        }

        if (recipe.Branch == PersistentCraftBranch.Anomaly)
        {
            return category switch
            {
                "anomaly-detectors" => "scanner-line",
                "artifact-detectors" => "artifact-line",
                "containers" => "containers-line",
                _ => $"tier-{recipe.Tier}",
            };
        }

        return string.Empty;
    }

    private string GetCategoryName(string categoryId)
