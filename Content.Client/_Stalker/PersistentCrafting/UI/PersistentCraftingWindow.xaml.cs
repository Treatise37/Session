        }
        else
        {
            body.AddChild(new Label
            {
                Text = Loc.GetString("persistent-craft-node-cost", ("cost", node.Cost)),
                FontColorOverride = MutedTextColor,
                HorizontalAlignment = HAlignment.Center,
            });
        }

        button.AddChild(body);
        return button;
    }

