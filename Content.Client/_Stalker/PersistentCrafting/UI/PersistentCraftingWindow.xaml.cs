    public void UpdateState(
        PersistentCraftState state,
        IEnumerable<PersistentCraftNodePrototype> nodes,
        IEnumerable<PersistentCraftRecipePrototype> recipes,
        Action<string> onUnlock)
    {
        _state = state;
        _nodes = nodes
            .OrderBy(node => node.Branch)
            .ThenBy(node => node.Tier)
            .ThenBy(node => node.SubTier)
            .ThenBy(node => node.ID)
            .ToList();
        _recipes = recipes
            .OrderBy(recipe => recipe.Branch)
            .ThenBy(recipe => recipe.Tier)
            .ThenBy(recipe => recipe.SubTier)
            .ThenBy(recipe => recipe.ID)
            .ToList();
        _onUnlock = onUnlock;

        if (state.Loaded && _selectPreferredBranchOnNextUpdate)
        {
            SelectPreferredBranchTab();
            _selectPreferredBranchOnNextUpdate = false;
        }

        Render();
    }

    public void ResetInitialTabSelection()
    {
        _selectPreferredBranchOnNextUpdate = true;
    }

    private void Render()
    {
        if (_state == null)
            return;

        RenderHeader(_state);
        RenderBranch(GetCurrentBranch());
    }

    private void RenderHeader(PersistentCraftState state)
    {
        if (!state.Loaded)
        {
            PointsLabel.Text = Loc.GetString("persistent-craft-window-loading-points");
            SummaryLabel.SetMarkup(Loc.GetString("persistent-craft-window-loading"));
            return;
        }

        PointsLabel.Text = Loc.GetString("persistent-craft-points", ("points", BuildPointsSummary(state)));
        SummaryLabel.SetMarkup(BuildSummaryMarkup(state));
    }

    private void RenderBranch(PersistentCraftBranch branch)
    {
        var (tierHost, subNodeHost, detailHost) = GetBranchHosts(branch);
        tierHost.RemoveAllChildren();
        subNodeHost.RemoveAllChildren();
        detailHost.RemoveAllChildren();

        if (_state == null)
            return;

        var branchNodes = _nodes.Where(node => node.Branch == branch).ToList();
        var mainTiers = branchNodes
            .Where(PersistentCraftingHelper.IsMainTierNode)
            .OrderBy(node => node.Tier)
            .ToList();

        if (mainTiers.Count == 0)
            return;

        var branchState = GetBranchState(_state, branch);
        var selectedTier = ResolveSelectedTier(branch, mainTiers);
        var subNodes = branchNodes
            .Where(node => !PersistentCraftingHelper.IsMainTierNode(node) && node.Tier == selectedTier.Tier)
            .OrderBy(node => node.SubTier)
            .ToList();
        var selectedNode = ResolveSelectedNode(branch, selectedTier, subNodes);

        tierHost.AddChild(CreateBranchSummary(branch, branchState));
        tierHost.AddChild(new Control { MinSize = new Vector2(1, 10) });
        tierHost.AddChild(CreateSectionHeader(Loc.GetString("persistent-craft-main-tier-label")));
        tierHost.AddChild(new Control { MinSize = new Vector2(1, 8) });
        tierHost.AddChild(CreateMainTierList(branch, mainTiers, selectedTier));

        subNodeHost.AddChild(CreateSectionHeader(Loc.GetString("persistent-craft-subnodes-label")));
        subNodeHost.AddChild(new Control { MinSize = new Vector2(1, 8) });
        if (subNodes.Count == 0)
        {
            subNodeHost.AddChild(new Label
            {
                Text = Loc.GetString("persistent-craft-none"),
                FontColorOverride = MutedTextColor,
            });
        }
        else
        {
            subNodeHost.AddChild(CreateSubNodeTree(branch, subNodes, selectedNode));
        }

        detailHost.AddChild(CreateDetailsPanel(branchState, selectedNode, subNodes));
    }

    private string BuildPointsSummary(PersistentCraftState state)
    {
        return string.Join(" | ", PersistentCraftingHelper.EnumerateBranches().Select(branch =>
        {
            var branchState = GetBranchState(state, branch);
            return $"{Loc.GetString(PersistentCraftingHelper.GetBranchLocKey(branch))}: {branchState.AvailablePoints}";
        }));
    }

    private string BuildSummaryMarkup(PersistentCraftState state)
    {
        var lines = PersistentCraftingHelper.EnumerateBranches().Select(branch =>
        {
            var branchState = GetBranchState(state, branch);
            return $"[color={MutedTextColor.ToHex()}]{Loc.GetString(PersistentCraftingHelper.GetBranchLocKey(branch))}: " +
                   $"{PersistentCraftingHelper.FormatLevel(branchState.Level, branchState.SubLevel)} | " +
                   $"{Loc.GetString("persistent-craft-experience-label")}: {branchState.Experience}/{branchState.ExperienceForNextLevel} | " +
                   $"{Loc.GetString("persistent-craft-points-short")}: {branchState.AvailablePoints}[/color]";
        }).ToList();

        lines.Add($"[color={MutedTextColor.ToHex()}]{Loc.GetString("persistent-craft-unlocked-summary", ("unlocked", state.UnlockedNodes.Count))}[/color]");
        return string.Join("\n", lines);
    }

